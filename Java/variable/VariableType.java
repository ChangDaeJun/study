package Java.variable;

public class VariableType {
    public static void main(String[] args) {
        //primitive type
        boolean p1 = true; // 1바이트
        char p2 = 'a'; // 2바이트
        byte p3 = 1; //1바이트
        short p4 = 1;//2바이트
        int p5 = 1; // 4바이트
        long p6 = 1;//8바이트
        float p7 = 1;// 4바이트
        double p8 = 1;//8바이트
    }
}
